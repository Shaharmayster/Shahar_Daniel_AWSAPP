def format_recipient(recipient):
    mapping = {
        "נכדים": "נכדים שלי היקרים",
        "נכד": "הנכד שלי היקר",
        "נכדה": "הנכדה שלי היקרה",
        "משפחה": "המשפחה שלי היקרה",
        "בן": "הבן שלי היקר",
        "בת": "הבת שלי היקרה",
    }
    return mapping.get(recipient, recipient)


